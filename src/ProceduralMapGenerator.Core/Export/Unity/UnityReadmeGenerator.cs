        sb.AppendLine("```csharp");
        sb.AppendLine("using Newtonsoft.Json;");
        sb.AppendLine();
        sb.AppendLine("TextAsset jsonAsset = Resources.Load<TextAsset>(\"dungeon_seed_42\");");
        sb.AppendLine("UnityMapData data   = JsonConvert.DeserializeObject<UnityMapData>(jsonAsset.text);");
