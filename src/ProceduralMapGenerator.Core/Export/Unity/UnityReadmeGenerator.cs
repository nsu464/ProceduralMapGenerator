        sb.AppendLine();
        sb.AppendLine("### Option B — Newtonsoft.Json (recommended)");
        sb.AppendLine();
        sb.AppendLine("Install via **Window → Package Manager → Add by name**: `com.unity.nuget.newtonsoft-json`");
