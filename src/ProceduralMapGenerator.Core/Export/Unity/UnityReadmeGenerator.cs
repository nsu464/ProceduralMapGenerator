using System.Text;
using ProceduralMapGenerator.Core.Models;

namespace ProceduralMapGenerator.Core.Export.Unity;

public static class UnityReadmeGenerator
{
    public static string GenerateReadme(Map map, int seed, string generatorType)
    {
        var sb = new StringBuilder();

        Title(sb);
        ImportGuide(sb);
        Structs(sb);
        ExampleMonoBehaviour(sb);
        TilemapTip(sb, generatorType);
        Stats(sb, map, seed, generatorType);

        return sb.ToString();
    }

